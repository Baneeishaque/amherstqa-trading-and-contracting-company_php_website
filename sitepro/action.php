if ($_POST && isset($_FILES['my_file'])) {

    $from_email = 'noreply@amherstqa.com'; //from mail, it is mandatory with some hosts
    $recipient_email = 'info@amherstqa.com'; //recipient email (most cases it is your personal email)
    //Capture POST data from HTML form and Sanitize them, 
    $sender_name = filter_var($_POST["sender_name"], FILTER_SANITIZE_STRING); //sender name
    $reply_to_email = filter_var($_POST["sender_email"], FILTER_SANITIZE_STRING); //sender email used in "reply-to" header
    $sender_place = filter_var($_POST["place"], FILTER_SANITIZE_STRING); //sender name
    $sender_gender = filter_var($_POST["gender"], FILTER_SANITIZE_STRING); //sender name

    $sender_qual = filter_var($_POST["qualify"], FILTER_SANITIZE_STRING); //sender name
    $sender_nation = filter_var($_POST["nation"], FILTER_SANITIZE_STRING); //sender name
    $sender_job = filter_var($_POST["job"], FILTER_SANITIZE_STRING); //sender name
    $sender_phone = filter_var($_POST["phoneno"], FILTER_SANITIZE_STRING); //sender name
    $sender_addr = filter_var($_POST["address"], FILTER_SANITIZE_STRING); //sender name


    $subject = filter_var($_POST["subject"], FILTER_SANITIZE_STRING); //get subject from HTML form
    $message = filter_var($_POST["message"], FILTER_SANITIZE_STRING); //message

    /* //don't forget to validate empty fields 
      if(strlen($sender_name)<1){
      die('Name is too short or empty!');
      }
     */

    $msg = 'NAME:' . $sender_name . "\r\n"
        . 'PHONE NUBER:' . $sender_phone . "\r\n"
        . 'PLACE:' . $sender_place . "\r\n"
        . 'EMAIL:' . $reply_to_email . "\r\n"
        . 'QUALIFICATION:' . $sender_qual . "\r\n"
        . 'NATIONALITY:' . $sender_nation . "\r\n"
        . 'JOB:' . $sender_job . "\r\n"
        . 'GENDER:' . $sender_gender . "\r\n"
        . 'MESSAGE:' . $sender_addr . "\r\n";


    //Get uploaded file data
    $file_tmp_name = $_FILES['my_file']['tmp_name'];
    $file_name = $_FILES['my_file']['name'];
    $file_size = $_FILES['my_file']['size'];
    $file_type = $_FILES['my_file']['type'];
    $file_error = $_FILES['my_file']['error'];

    if ($file_error > 0) {
        die('Upload error or No files uploaded');
    }
    //read from the uploaded file & base64_encode content for the mail
    $handle = fopen($file_tmp_name, "r");
    $content = fread($handle, $file_size);
    fclose($handle);
    $encoded_content = chunk_split(base64_encode($content));

    $boundary = md5("sanwebe");
    //header
    $headers = "MIME-Version: 1.0\r\n";
    $headers .= "From:" . $from_email . "\r\n";
    $headers .= "Reply-To: " . $reply_to_email . "" . "\r\n";
    $headers .= "Content-Type: multipart/mixed; boundary = $boundary\r\n\r\n";

    //plain text 
    $body = "--$boundary\r\n";
    $body .= "Content-Type: text/plain; charset=ISO-8859-1\r\n";
    $body .= "Content-Transfer-Encoding: base64\r\n\r\n";
    $body .= chunk_split(base64_encode($msg));


    //attachment
    $body .= "--$boundary\r\n";
    $body .= "Content-Type: $file_type; name=" . $file_name . "\r\n";
    $body .= "Content-Disposition: attachment; filename=" . $file_name . "\r\n";
    $body .= "Content-Transfer-Encoding: base64\r\n";
    $body .= "X-Attachment-Id: " . rand(1000, 99999) . "\r\n\r\n";
    $body .= 'NAME:' . $sender_name;
    $body .= $encoded_content;


    $sentMail = @mail($recipient_email, $subject, $body, $headers);
    if ($sentMail) { //output success or failure messages
        die('Thank you for your email');
    } else {
        die('Could not send mail! Please check your PHP mail configuration.');
    }
}
